#include "3-calc.h"

/**
 * op_add - addition of two integers
 * @a: input number 1
 * @b: input number 2
 * Return: the sum of a and b
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - substraction of two integers
 * @a: input number 1
 * @b: input number 2
 * Return: the difference of a and b
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - nultiplication of two integers
 * @a: input number 1
 * @b: input number 2
 * Return: the multiplication of a and b
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
  * op_div - division of two integers
  * @a: input number 1
  * @b: input number 2
  * Return: the division of a and b
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
  * op_mod - modulo of two integers
  * @a: input number 1
  * @b: input number 2
  * Return: the remainder of a and b
 */
int op_mod(int a, int b)
{
	return (a % b);
}
